namespace RefDocGen.DocExtraction.Tools;

/// <summary>
/// Contains constants representing XML element and attribute names found in XML doc comments.
/// </summary>
internal static class XmlDocIdentifiers
{
    /// <summary>
    /// Represents the 'member' identifier.
    /// </summary>
    public const string Member = "member";

    /// <summary>
    /// Represents the 'summary' identifier.
    /// </summary>
    public const string Summary = "summary";

    /// <summary>
    /// Represents the 'name' identifier.
    /// </summary>
    public const string Name = "name";

    /// <summary>
    /// Represents the 'param' identifier.
    /// </summary>
    public const string Param = "param";

    /// <summary>
    /// Represents the 'returns' identifier.
    /// </summary>
    public const string Returns = "returns";
}
